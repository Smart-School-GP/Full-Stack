const roleColors: Record<string, string> = {
  admin: 'from-purple-600 to-purple-800',
  teacher: 'from-brand-600 to-brand-800',
  parent: 'from-emerald-600 to-emerald-800',
  student: 'from-amber-500 to-amber-700',
}

export default function Sidebar() {
  const pathname = usePathname()
  const router = useRouter()
  const [mounted, setMounted] = useState(false)
  const [user, setUser] = useState<{ name?: string; role?: string } | null>(null)

  useEffect(() => {
    setMounted(true)
    setUser(getUser())
  }, [])

  const role = mounted ? (user?.role || 'student') : 'student'
  const items = navItems[role] || []

  const handleLogout = () => {
    clearAuth()
    router.push('/login')
  }

  return (
    <aside className="w-64 h-screen bg-slate-900 flex flex-col">
      {/* Brand */}
      <div className={`p-5 bg-gradient-to-r ${roleColors[role]}`}>
        <div className="flex items-center gap-3">
          <div className="w-9 h-9 bg-white/20 rounded-xl flex items-center justify-center">
            <svg className="w-5 h-5 text-white" fill="none" viewBox="0 0 24 24" stroke="currentColor">
              <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M12 14l9-5-9-5-9 5 9 5z" />
            </svg>
          </div>
          <div>
            <p className="text-white font-bold text-sm leading-tight">School Ecosystem</p>
            <p className="text-white/70 text-xs capitalize">{role} Portal</p>
          </div>
        </div>
      </div>

      {/* User info + notification bell */}
      <div className="px-5 py-4 border-b border-slate-700/50 flex items-center justify-between">
        <div className="min-w-0">
          <p className="text-slate-200 font-medium text-sm truncate">
            {mounted && user?.name ? user.name : 'Loading...'}
          </p>
          <span className={`badge mt-1 ${
            role === 'admin' ? 'badge-admin' :
            role === 'teacher' ? 'badge-teacher' :
            role === 'parent' ? 'badge-parent' : 'badge-student'
          }`}>
            {role}
          </span>
        </div>
        <div className="flex items-center gap-2">
          <ThemeToggle />
          <NotificationBell />
        </div>
      </div>

      {/* Nav */}
      <nav className="flex-1 p-3 space-y-1">
        {items.map((item) => {
          const active = pathname === item.href || pathname.startsWith(item.href + '/')
          return (
            <Link
              key={item.href}
              href={item.href}
              className={`flex items-center gap-3 px-3 py-2.5 rounded-lg text-sm font-medium transition-colors ${
                active
                  ? 'bg-white/10 text-white'
                  : 'text-slate-400 hover:text-slate-200 hover:bg-white/5'
              }`}
            >
              {item.icon}
              {item.label}
            </Link>
          )
        })}
      </nav>

      {/* Logout */}
      <div className="p-3 border-t border-slate-700/50">
        <button
          onClick={handleLogout}
          className="flex items-center gap-3 w-full px-3 py-2.5 rounded-lg text-sm font-medium text-slate-400 hover:text-red-400 hover:bg-red-500/10 transition-colors"
        >
          <svg className="w-5 h-5" fill="none" viewBox="0 0 24 24" stroke="currentColor">
            <path strokeLinecap="round" strokeLinejoin="round" strokeWidth={2} d="M17 16l4-4m0 0l-4-4m4 4H7m6 4v1a3 3 0 01-3 3H6a3 3 0 01-3-3V7a3 3 0 013-3h4a3 3 0 013 3v1" />
          </svg>
          Sign out
        </button>
      </div>
    </aside>
  )
}

